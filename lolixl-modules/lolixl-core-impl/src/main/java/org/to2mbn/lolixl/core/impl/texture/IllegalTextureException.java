package org.to2mbn.lolixl.core.impl.texture;

public class IllegalTextureException extends IllegalArgumentException {

	private static final long serialVersionUID = 1L;

	public IllegalTextureException() {}

	public IllegalTextureException(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalTextureException(String s) {
		super(s);
	}

	public IllegalTextureException(Throwable cause) {
		super(cause);
	}

}
